# Using  Tuples in Python

# Tuples are just like lists but instead of using [] we use () and no quotation marks incase we're using integers

Tuple1 =(1,2)
print(Tuple1[1]) #prints out 2 as its index is 1
print(Tuple1[0]) #prints out 1 as its index is 0

# Using a for loop with tuples

for Tuple1 in Tuple1:
    print(Tuple1) #output is 1,2 in their own lines

# Modifying a tuple
Tuple1 = (10,20,30,40,50,60)# auto updates and deletes items in the tuple replacing them with new ones
print(Tuple1) # output is (10, 20, 30, 40, 50, 60)